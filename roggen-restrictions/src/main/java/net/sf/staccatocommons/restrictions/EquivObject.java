package net.sf.staccatocommons.restrictions;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Inherited;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * {@link EquivObject} are objects that implement {@link #equals(Object)} and
 * {@link #hashCode()} consistently and not based on identity but rather on
 * state.
 * 
 * {@link EquivObject} <strong>should</strong> be also {@link ValueObject}s.
 * 
 * @since 3
 */
@Documented
@Inherited
@Restriction
@Retention(RetentionPolicy.SOURCE)
@Target(ElementType.TYPE)
public @interface EquivObject {

}
